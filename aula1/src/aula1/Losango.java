package aula1;

public class Losango extends Poligono {
	  private double v; //volume
	  private double a;
      private double l;
      private double b;
      private double h;
      public Losango(double a){
      	this.l =b*h;
      }
      @Override
      public double a(){
      	return a = l;
      }
      @Override
      public double v(){
      	return v = v+a;
      }
}
