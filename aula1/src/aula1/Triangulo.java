package aula1;

public class Triangulo extends Poligono {
    protected double v; //volume
	private double t; 
    protected double b;
    public double h;
    protected double a;
    public Triangulo(double t){
    	this.t =(b*h)/2;
    	this.v = b*h;
    }
    @Override
    public double a(){
    	return a = t;
    }
    @Override
    public double v(){
    	return v = v+a;
    }

}   
