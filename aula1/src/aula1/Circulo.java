package aula1;

public class Circulo extends Figura {
        private double c;
	    protected double a;
	    private double p;
	    protected double r;
	    protected double v;
	    protected double b;
	    protected double h;
	    public Circulo(double c){
	    	this.c = p * Math.pow(r,2);
	    	this.p = 3.14;
	    	this.v = b*h;
	    }
	    @Override
	    public double a(){
	    	return a = c;
	    } 
	    @Override
	      public double v(){
	      	return v = v+a;
	      }
}
