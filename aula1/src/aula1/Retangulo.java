package aula1;

public class Retangulo extends Poligono {
	 private double a;
	 private double r;
	 private double v;
	 private double h;
	 private double b;
    public Retangulo(double a){
    	this.r = Math.sqrt(2);
    }
    @Override
    public double a(){
    	return a = r;
    } 
    @Override
    public double v(){
    	return v = b*h;
    }

}
