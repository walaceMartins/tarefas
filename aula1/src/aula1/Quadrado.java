package aula1;

public class Quadrado extends Poligono {
	protected double a;
	private double q;
    protected double h;
    protected double b;
    protected double v;
    public Quadrado(double a){
    	this.q = Math.pow(h,2);
    }
    @Override
    public double a(){
    	return a = q;
    } 
    @Override
    public double v(){
    	return v = b*h;
    }

}
